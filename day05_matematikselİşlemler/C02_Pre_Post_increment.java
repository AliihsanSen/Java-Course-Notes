package day05_matematikselİşlemler;

public class C02_Pre_Post_increment {

    public static void main(String[] args) {

        int sayi = 10;
        System.out.println("pre-increment : " + ++sayi); // 1- artırma
                                                         // 2-yazdırma


        System.out.println("post-increment : " + sayi++); // 1-yazdırma
                                                          //2-artırma

        System.out.println("post-increment'den sonra : " + sayi); //artırılan sayı


    }
}
